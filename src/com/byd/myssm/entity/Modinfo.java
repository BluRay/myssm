package com.byd.myssm.entity;

public class Modinfo {
	private int id;
	private String jjq_id;
	private String moder_name;
	private String company;
	private String jjq_no;
	private String moder_date;
	private String mod_info;
	private String price;
	private String s_price;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getJjq_id() {
		return jjq_id;
	}
	public void setJjq_id(String jjq_id) {
		this.jjq_id = jjq_id;
	}
	public String getModer_name() {
		return moder_name;
	}
	public void setModer_name(String moder_name) {
		this.moder_name = moder_name;
	}
	public String getCompany() {
		return company;
	}
	public void setCompany(String company) {
		this.company = company;
	}
	public String getJjq_no() {
		return jjq_no;
	}
	public void setJjq_no(String jjq_no) {
		this.jjq_no = jjq_no;
	}
	public String getModer_date() {
		return moder_date;
	}
	public void setModer_date(String moder_date) {
		this.moder_date = moder_date;
	}
	public String getMod_info() {
		return mod_info;
	}
	public void setMod_info(String mod_info) {
		this.mod_info = mod_info;
	}
	public String getPrice() {
		return price;
	}
	public void setPrice(String price) {
		this.price = price;
	}
	public String getS_price() {
		return s_price;
	}
	public void setS_price(String s_price) {
		this.s_price = s_price;
	}

}
